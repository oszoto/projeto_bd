    private void btnForneceProdAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnForneceProdAddActionPerformed
        ForneceProdutoDTO abastecimento = new ForneceProdutoDTO();
        abastecimento.setCnpj(txtForneceProdCnpj.getText());
        abastecimento.setCod_prod(txtForneceProdCod.getText());
        abastecimento.setDataHora(txtForneceProdDataHora.getText());
        abastecimento.setQtd(txtForneceProdQtd.getText());
        new ForneceProdutoDAO().abasteceProduto(abastecimento);
        listarAbastecimentos();
    }//GEN-LAST:event_btnForneceProdAddActionPerformed

    private void listarAbastecimentos(){
        try{
            ForneceProdutoDAO forneceProdutoDAO = new ForneceProdutoDAO();
            DefaultTableModel model = (DefaultTableModel) tblForneceProd.getModel();
            model.setNumRows(0);
            ArrayList<ForneceProdutoDTO> abastecimentos = forneceProdutoDAO.histAbastecimento();
            for (int n = 0; n < abastecimentos.size(); n++){
                model.addRow(new Object[]{
                    abastecimentos.get(n).getCnpj(),
                    abastecimentos.get(n).getCod_prod(),
                    abastecimentos.get(n).getDataHora(),
                    abastecimentos.get(n).getQtd()
                });
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "HistÃ³rico de abastecimentos " + erro.getMessage());
        }
    }
    
